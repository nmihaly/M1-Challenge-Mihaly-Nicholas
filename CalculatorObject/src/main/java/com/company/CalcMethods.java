package com.company;

public class CalcMethods {

    public static int add(int int1) {
        return int1 + int1;
    }

    public static int subtract(int int2, int int3) {
        return int2 - int3;
    }

    public static int multiply(int int4, int int5) {
        return int4 * int5;
    }

    public static int divide(int int6, int int7) {

        if(int7 == 0)
            throw new IllegalArgumentException("Cannot divide by zero!");
        return int6 / int7;

    }

    public static int divide2(int int6, int int8) {
        return int6 / int8;
    }


    public static double add(double double1, double double2) {
        return double1 + double2;
    }
    public static double multiply(double double3, double double4) {
        return double3 * double4;
    }
    public static double subtract(double double5, double double6) {
        return double5 - double6;
    }
    public static double divide(double double7, double double8) {

        if (double8 == 0)
            throw new IllegalArgumentException("Cannot divide by zero!");
        return double7 / double8;

    }


}
